	<div id="help-rules" class="helpbox">
		<h4 class="helpbox-title block_title_bar">Hold'em Regeln</h4>
		<div class="content">
			<p>Bei Texas Hold’em Poker erhält jeder Spieler zunächst 2 Karten verdeckt, die so genannten <em>hole cards</em>. Weitere 5 Gemeinschaftskarten (<em>Community Cards</em>) werden in der Tischmitte aufgedeckt. <em>Hole cards</em> und Gemeinschaftskarten können in beliebiger Kombination verwendet werden um das beste 5-Karten-Blatt aus insgesamt 7 Karten zu bilden.</p>

			<p><em>Dealer Button:</em> Markiert die Position wo das Austeilen der Karten beginnt. Auf der Position nach dem Dealer Button beginnt immer die Wettrunde. Nach jeder gespielten Hand wandert der Dealer Button eine Position weiter nach links.</p>
			<p><em>Small Blind:</em> Kleiner Pflichteinsatz den der auf den Dealer folgende Spieler zu Beginn der ersten Wettrunde zu zahlen hat.</p>
			<p><em>Big Blind:</em> Grosser Pflichteinsatz den der auf den Small Blind folgende Spieler zu Beginn der ersten Wettrunde zu zahlen hat.</p>
			<ul class="helpbox-list">
				<li><strong>Erste Wettrunde – Hole Cards</strong><br />
				Nachdem jeder Spieler 2 verdeckte Karten erhalten hat, beginnt der nächste Spieler nach dem Big Blind die 1. Wettrunde. Die Spieler haben folgende Möglichkeiten: Mitgehen (call), Erhöhen (raise) oder Passen (fold). Jeder Spieler muss denselben Betrag an Chips in den Pot zahlen. Erhöht ein Spieler den Einsatz müssen folglich die anderen Spieler entweder mitgehen oder passen oder wiederum den Einsatz erhöhen. Ein Spieler der passt, scheidet aus der Runde aus.</li>
				<li><strong>Zweite Wettrunde - Flop</strong><br />
				Drei Karten werden als Gemeinschaftskarten aufgedeckt. Jeder Spieler kann diese in Kombination mit seinen beiden verdeckten Karten verwenden. Die verbliebenen Spieler beginnen nun mit der 2. Wettrunde. Der auf den Dealer folgende Spieler beginnt. Da im Gegensatz zur 1. Runde noch kein Einsatz erbracht wurde, hat er die Option zu schieben. (checken). Alle folgenden Spieler können solange checken wie kein Einsatz erbracht ist. Setzt ein Spieler so bleiben Mitgehen und Erhöhen als Option um im Spiel zu bleiben. Natürlich kann der Spieler der an der Reihe ist jederzeit passen.</li>
				<li><strong>Dritte Wettrunde - Turn</strong><br />
				Eine weitere Gemeinschaftskarte, der Turn, wird aufgedeckt sodass den Spielern nun insgesamt 6 Karten zur Verfügung stehen um eine Kombination aus 5 Karten zu bilden. Eine weitere Wettrunde beginnt nach denselben Regeln wie in Runde 2.</li>
				<li><strong>Vierte Wettrunde – River</strong><br />
				Nun folgt die 5. und letzte Gemeinschaftskarte, der River. Die letzte Wettrunde beginnt.</li>
  				<li><strong>Showdown</strong><br />
				Unter den verbleibenden Spielern, die nicht gefoldet haben wird der Gewinner mit dem besten Blatt aus 5 Karten ermittelt. Dabei spielt es keine Rolle wie viele Karten er von seinen hole cards dafür verwendet hat.</li>
			</ul>
		</div>
	</div>

	<div id="help-tips" class="helpbox">
		<h4 class="helpbox-title block_title_bar">Tipps</h4>
		<div class="content">
		<ol class="helpbox-list">
			<li><strong>Spiele ausgewählte Hände</strong><br />
			Geduld führt zu erfolgreichem Spiel. Poker-Profis schauen sich nur in 20-30% der Fälle den Flop an. Es lohnt sich, auf gute Starthände zu warten, um den Glücksfaktor zu minimieren.</li>
			<li><strong>Folden ist keine Schande</strong><br />
			Als Anfänger lässt man sich viel zu oft in hohe Einsätze herein ziehen. Oft entsprechen die zu bringenden Chips in keiner Weise dem zu erwartenden Gewinn und der Wertigkeit der eigenen Hand. Zu wissen, wann man foldet, ist einer der Schlüssel zu einem guten Spiel.</li>
			<li><strong>Beobachte deine Mitspieler</strong><br />
			Du solltest das Spiel Deiner Gegner immer aufmerksam verfolgen, besonders wenn Du selber nicht mitgehst, da dadurch eine objektivere Einschätzung möglich ist. Jede Information, die man aus der Beobachtung seiner Gegenspieler sammeln kann, ist wertvoll.</li>
			<li><strong>Die Kunst des Setzens</strong><br />
			Man setzt nicht nur, wenn man eine gute Hand hat. Es gibt verschiedene Techniken des Setzens, um an Informationen über seine Gegenspieler zu gelangen. Mit einem re-raise (nochmaliges Erhöhen) zum Beispiel kannst Du die Glaubwürdigkeit des Einsatzes eines Mitspielers testen.</li>
			<li><strong>Der Bluff</strong><br />
			Ein Bluff kann einer der mächtigsten Spielzüge im Poker sein. Zur richtigen Zeit eingesetzt, kann man durch Bluffs nicht nur einzelne Runden für sich entscheiden, auch ein fehlgeschlagener Bluff kann den Gegner dazu bringen, bei späteren Runden auch wieder einen Bluff zu vermuten, und ihn so zu höheren Einsätzen ermutigen. Jedoch sollte bei solch risikoreichen Aktionen immer das Verhältnis von Risiko, Einsatz und Gewinn stimmen.</li>
			<li><strong>Variiere deine Speilweise</strong><br />
			Wenn Du Deine Spielweise häufiger wechselst, bleibst Du unberechenbar für Deine Gegenspieler. Es ist schwieriger die Stärke deiner Hand zu deuten.</li>
			<li><strong>Passe dein Spiel deiner Position am Tisch an</strong><br />
			Es ist von Vorteil eine gute Position am Tisch zu nutzen. Bist Du zum Beispiel "on the Button", also der Dealer, kannst Du Informationen und Einschätzungen aus der Verhaltensweise deiner Gegner sammeln, da du als letzter in der Setzrunde Deine Entscheidung treffen musst.</li>
			<li><strong>Man lernt nie aus</strong><br />
			Die Poker-Regeln sind schnell gelernt und Spaß macht es von Anfang an, da auch der absolute Anfänger gegen einen Profi gewinnen kann. Spannend bleibt es aber immer, da man beim Poker durch Erfahrung ständig sein Spiel verbessert. Daher der beste Tipp: spielen, spielen, spielen!</li>
		</ol>
		</div>
