	<div id="help-pokerhands" class="helpbox">
		<h4 class="helpbox-title block_title_bar">Pokerblätter</h4>
		<dl class="helpbox-list">
			<dt>Royal Flush
				<span class="poker-hand royal-flush" title="Royal Flush">A,K,Q,J,10</span>
			</dt>
			<dd>Ein Royal Flush ist ein Straight Flush mit einem Ass als höchstem Kartenwert: Zehn, Bube, Dame, König, Ass – alle Karten in Pik oder alle Karten in Kreuz usw.</dd>
			<dt>Straight Flush
				<span class="poker-hand straight-flush" title="Straight Flush">4,5,6,7,8</span>
			</dt>
			<dd>Ein Straight Flush ist eine Kombination aus Flush und Straße. Somit sind alle Karten von derselben Farbe und aufeinander folgend. Der Rang mehrerer Straßen untereinander richtet sich nach dem Wert der jeweils höchsten Karte der Straße. 87654 wird von 98765 geschlagen. Die Farbe hat keinen Einfluss auf den Wert.</dd>
    		<dt>Quads / Poker / Vierling
				<span class="poker-hand four-of-a-kind" title="Quads/Poker/Vierlinf">Q,Q,Q,Q</span>
			</dt>
			<dd>Alle vier Karten sind von gleichem Wert, wobei der Rang sich nach der Höhe des Wertes richtet. </dd>
			<dt>Full House
				<span class="poker-hand full-house" title="Full House">A,A,A,4,4</span>
			</dt>
			<dd>Ein Full House ist eine Kombination aus einem Drilling und einem Paar. Also 3 Karten von gleichem Wert plus 2 Karten von gleichem Wert. Ein Gleichstand bei einem Full House wird nach dem Wert der Drillinge entschieden.</dd>
			<dt>Flush
				<span class="poker-hand flush" title="Flush">K,D,10,4,2</span>
			</dt>
			<dd>Fünf Karten einer Farbe. Bei einem Gleichstand zählt der Wert der höchsten Poker Karte. Sollten also z.B. die ersten vier Karten den gleichen Wert aufweisen, zählt die höchste fünfte. Sind alle fünf Karten gleich, wird der Pot aufgeteilt, da die Farben gleichwertig sind.</dd>
			<dt>Strasse
				<span class="poker-hand straight" title="Strasse">10,J,Q,K,A</span>
			</dt>
			<dd>Eine Straße ist ein Blatt, bei dem alle Karten aufeinander folgen (Staight), jedoch nicht alle Karten von einer Farbe sind. Die Straße, die auf der höchsten Karte endet, gewinnt. Bei Gleichheit gilt ein Unentschieden. Der Pot wird geteilt. Bei einer Straße kann das Ass entweder als Ass oder als 1 zählen. Daraus ergibt sich die von ihrer Wertigkeit niedrigste Straße (A, 2, 3, 4, 5) und die höchste mit (10, J, Q, K, A). </dd>
			<dt>Drilling
				<span class="poker-hand three-of-a-kind" title="Drilling">K,K,K</span>
			</dt>
			<dd>Drei Karten gleichen Wertes. Der höchste Drilling gewinnt bei Drilling gegen Drilling. </dd>
			<dt>Zwei Paare
				<span class="poker-hand two-pairs" title="2 Paare">A,A,9,9</span>
			</dt>
			<dd>Der Spieler mit dem höchsten Paar gewinnt wenn mehrere Spieler zwei Paare halten. Wenn auch das höchste Paar gleich ist, zählt das zweithöchste. Sollte auch dieses gleich sein, zählt die fünfte Karte als so genannter Kicker.</dd>
			<dt>Paar
				<span class="poker-hand one-pair" title="Paar">A,A</span>
			</dt>
		    <dd>Zwei Karten gleichen Wertes. Der Spieler mit dem höchsten Paar gewinnt, wenn mehrere Spieler ein Paar halten.</dd>
			<dt>Höchst Karte
				<span class="poker-hand high-card" title="Höchste Karte">A</span>
			</dt>
			<dd>Die höchste Poker-Karte gewinnt, wenn kein Spieler ein Paar oder ein höheres Blatt hält.</dd>
		</dl>
